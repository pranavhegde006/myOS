#ifndef TYPES_H
#define TYPES_H

namespace myOS
{
    namespace common
    {
        // n bits signed type - s<n>_t
        // n bits unsigned type - u<n>_t

        typedef char s8_t;
        typedef unsigned char u8_t;

        typedef short s16_t;
        typedef unsigned short u16_t;

        typedef int s32_t;
        typedef unsigned int u32_t;

        typedef long long int s64_t;
        typedef unsigned long long int u64_t;
    }   
}  
 

#endif